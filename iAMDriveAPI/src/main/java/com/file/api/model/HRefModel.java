package com.file.api.model;

public class HRefModel {
	
	private String hrefFile;
	private String hrefFileText;
	private String hrefDelete;
	public String getHrefFile() {
		return hrefFile;
	}
	public void setHrefFile(String hrefFile) {
		this.hrefFile = hrefFile;
	}
	public String getHrefFileText() {
		return hrefFileText;
	}
	public void setHrefFileText(String hrefFileText) {
		this.hrefFileText = hrefFileText;
	}
	public String getHrefDelete() {
		return hrefDelete;
	}
	public void setHrefDelete(String hrefDelete) {
		this.hrefDelete = hrefDelete;
	}
	
	

}
